function setupCarousel(rootSelector){
  document.querySelectorAll(rootSelector).forEach(carousel => {
    const track = carousel.querySelector('.carousel-track');
    const wrap = carousel.querySelector('.carousel-track-wrap');
    if(!track || !wrap) return; // evita erros se estrutura diferente

    const prev = carousel.querySelector('.prev');
    const next = carousel.querySelector('.next');
    let index = 0;

    function stepWidth(){
      const first = track.children[0];
      if(!first) return 0;
      const gap = parseFloat(getComputedStyle(track).gap) || 0;
      return first.getBoundingClientRect().width + gap;
    }

    function maxIndex(){
      const first = track.children[0];
      if(!first) return 0;
      const visible = Math.max(1, Math.floor(wrap.clientWidth / first.getBoundingClientRect().width));
      return Math.max(0, track.children.length - visible);
    }

    function update(){
      index = Math.max(0, Math.min(index, maxIndex()));
      track.style.transform = `translateX(${-index * stepWidth()}px)`;
    }

    if(prev) prev.addEventListener('click', () => { index = Math.max(0, index - 1); update(); });
    if(next) next.addEventListener('click', () => { index = Math.min(maxIndex(), index + 1); update(); });

    window.addEventListener('resize', update);
    update();
  });
}

