/* utilitário: tenta obter elemento por id várias vezes antes de desistir */
function waitForEl(id, cb, attempts = 40, interval = 60){
  let tries = 0;
  const tick = () => {
    const el = document.getElementById(id);
    if(el){ cb(el); return; }
    tries++;
    if(tries >= attempts){
      console.warn(`waitForEl: elemento "${id}" não encontrado após ${tries} tentativas.`);
      return;
    }
    setTimeout(tick, interval);
  };
  tick();
}

